# 1. Buatlah sebuah program 
# untuk menghitung Luas Lingkaran
# dengan menggunakan user input!

jari_jari = float(input('Masukan jari-jari:\n> '))
pi = 22 / 7
luas = pi * jari_jari ** 2
print('Anda memasukan jari-jari', jari_jari, 
'cm.\nJadi, luas lingkaran adalah', luas,'cm2')


# 2. Buat program Konverter Fahrenheit
# ke Celcius

fah = float(input('Masukan derajat fahrenheit:\n> '))
cel = (fah - 32) * 5/9
print(fah, 'F ke Celcius adalah :', cel, 'C')





















