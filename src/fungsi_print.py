# / outputnya float number
# // outputnya integer number
print(7 / 3)
print(7 // 3)
print(-7 / 3)
print(-7 // 3)

# ** pangkat
print(2**8)

# \ escape character
print('I\'m Fikky')

# \n baris baru
print('Hallo\nSaya Fikky')

# \t tab baru
print('Satu Dua\n\tTiga')

# Latihan:
# Satu, 'dua', "tiga", empat, \lima\
#     enam, 'Aku' sayang
#         '//semuanya\\'
print('Satu, \'dua\', \"tiga\", empat, \\lima\\,\n\tenam,\'Aku\' sayang\n\t\t\'//semuanya\\\\\'')